def all_upper(text):
    """
    Convert all characters in the given text to uppercase.

    :param text: The input string to be converted to uppercase.
    :type text: str
    :return: The text converted to uppercase.
    :rtype: str
    """
    return text.upper()


def remove_whitespaces(text):
    """
    Remove leading and trailing whitespaces from the given text.

    :param text: The input string from which to remove whitespaces.
    :type text: str
    :return: The text with leading and trailing whitespaces removed.
    :rtype: str
    """
    return text.strip()
